package Calculator;

public class Calculator {
    double a, b;

    int gumarum(int a, int b) {
        return a + b;
    }

    int hanum(int a, int b) {
        return a - b;
    }

    double bajanum(double a, double b) {
        return a / b;
    }

    long bazmapatkum(int a, int b) {
        return a * b;

    }
}



