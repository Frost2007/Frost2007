public class Converter {


    double convertMileLength(double a) {
        return a * 0.621371;// miles
    }

    double convertKilometerLength(double a) {
        return a * 1.60934;// km
    }

    double convertLitersVol(double a) {
        return a * 3.78541;// liter
    }

    double convertGallonVol(double a) {
        return a * 0.264;// gallon
    }

    double convertPoundWeight(double a) {
        return a * 2.20462;// pound
    }

    double convertKgWeight(double a) {
        return a * 0.453592;// kg
    }


}
